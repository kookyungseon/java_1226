package exam02;

public class PrintTest02 {
	public static void main(String[] args) {
//		형식지정자(서식문자)
		
		System.out.printf("내 수학 점수는 %10d점 입니다\n", 100);
		System.out.printf("내 수학 점수는 %-10d점 입니다\n", 100);
		System.out.printf("내 평균 점수는 %10.2f점 입니다.\n", 88.155);
		System.out.printf("나의 학점은 %c 입니다.\n", 'A');
		System.out.printf("나의 이름은 %s 입니다", "구경선");
		
		
		
		
	}

}
