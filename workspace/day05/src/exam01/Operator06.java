package exam01;

public class Operator06 {
   public static void main(String[] args) {

      //비트 연산자
      System.out.println(10&11);
      System.out.println(10|11);
      System.out.println(10^11);
      System.out.println(~10); //-10-1 
      
//      쉬프트 연산자 : <<, >>
      System.out.println(10 >> 1);
      System.out.println(10 << 2);
      

   }

}