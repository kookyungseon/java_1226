package exam01;

public class Operator05 {
   public static void main(String[] args) {
      
//      복합대입 연산자
      int num1 = 10;
      num1 += 5; //num1 = num1 + 5;
      System.out.println(num1);
      
      int num2 = 2;
      int num3 = 4;
      
      num2 *= num3; //num2 = num2 * num3
      System.out.println(num2);
      
      int num4 = 10;
      int num5 = 3;
      num4 %= num5; //num4 = num4 % num5;
      System.out.println(num4);
      
   }

}