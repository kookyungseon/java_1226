package exam01;

public class TypeCasting03 {
	public static void main(String[] args) {
		
//		강제형변환
//		(자료형)값
		
		System.out.println((int)5.23); //double -> int : 자동형변환 되는것도 강제 변환 가능
		System.out.println((double)5); //정수 -> double
		System.out.println((char)97); //97 -> char
		System.out.println(10/(double)3); // 3을 강제 형변환, 정수 /실수 = 실수 (자동형변환)
		
		
	}

}
