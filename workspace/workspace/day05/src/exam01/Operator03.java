package exam01;

public class Operator03 {
   public static void main(String[] args) {
      
//      관계연산자
      int num1 = 10;
      int num2 = 3;
      
      System.out.println(num1 > num2);
      System.out.println(num1 < num2);
      System.out.println(num1 >= num2);
      System.out.println(num1 <= num2);
      
      System.out.println(num1 == num2);
      System.out.println(num1 != num2);
      
      boolean bo1 = num1 > num2;
      System.out.println(bo1);
      
   }

}