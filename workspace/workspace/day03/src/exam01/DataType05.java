package exam01;

public class DataType05 {
	public static void main(String[] args) {
//		논리형 boolean
		boolean bo1 = true;
		boolean bo2 = false;
		
		System.out.println(bo1);
		System.out.println(bo2);
		
		
//		상수
		final int NUMBER = 1;
//		NUMBER =10;
		final int FINAL_NUMBER = 10;
		
		System.out.println(NUMBER);
		System.out.println(FINAL_NUMBER);
		
	
		
	}

}
